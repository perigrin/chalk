# ABOUTME: SoN->LLVM IR lowering pass for the typed-representation model (Phase 3a/3b).
# ABOUTME: Lowers typed SoN graphs (Int/Num literals, Add/Sub/Mul/Div + Return) to LLVM IR text for lli.
package Chalk::IR::Target::LLVM;
use 5.42.0;
use utf8;

use Chalk::IR::Node::Coerce;

# lower($return_node) -> $llvm_ir_text
#
# Accepts a Return node whose value chain contains only Int-representation
# Constant and Add nodes (the literal-arithmetic slice). Emits LLVM IR text
# that, when run through lli, prints the computed integer value and exits 0.
#
# Observable output: printf("%d\n", result) so lli stdout matches perl's print.
# No libperl: this backend does not link or call any Perl C-API function.
# A value of representation Scalar reaching this backend is a GAP (not lowerable
# runtime-free); the lowering raises an error rather than silently emitting wrong
# code.
#
# Placement rationale: this is a production backend, not test infrastructure.
# lib/Chalk/IR/Target/ is the natural home for named lowering targets
# (parallel to the C/XS corner). Keeping it here avoids the drift pattern
# of temporary t/lib infrastructure that never gets promoted.
sub lower {
    my ($class, $return_node) = @_;

    my $ctx = Chalk::IR::Target::LLVM::Context->new;

    # Lower the value sub-graph rooted at the Return's input.
    my $value_node = $return_node->inputs->[0];
    my $result_ref = $ctx->lower_value($value_node);

    # Build the full LLVM IR module: header + main function body.
    my @lines;
    push @lines, '; Generated by Chalk::IR::Target::LLVM — SoN->LLVM lowering (Phase 3a)';
    push @lines, '; SoN graph: Return(Add(Constant(Int), Constant(Int))) with Int representation';
    push @lines, '';
    push @lines, '@fmt = private unnamed_addr constant [4 x i8] c"%d\0A\00", align 1';
    push @lines, '';
    push @lines, 'declare i32 @printf(i8* nocapture readonly, ...)';
    push @lines, '';
    push @lines, 'define i32 @main() {';
    push @lines, 'entry:';

    # Emit the instructions accumulated during value lowering.
    push @lines, $ctx->instructions->@*;

    # Print the result via printf.
    push @lines, "  %result_i32 = trunc i64 $result_ref to i32";
    push @lines, '  %fmt_ptr = getelementptr inbounds [4 x i8], [4 x i8]* @fmt, i64 0, i64 0';
    push @lines, '  call i32 (i8*, ...) @printf(i8* %fmt_ptr, i32 %result_i32)';
    push @lines, '  ret i32 0';
    push @lines, '}';

    return join("\n", @lines) . "\n";
}

# ---------------------------------------------------------------------------
# Internal lowering context
# ---------------------------------------------------------------------------
package Chalk::IR::Target::LLVM::Context;
use 5.42.0;
use utf8;

sub new {
    my ($class) = @_;
    return bless {
        instructions => [],
        counter      => 0,
        cache        => {},   # node id -> llvm_ref (%tmp_N)
    }, $class;
}

sub instructions { $_[0]->{instructions} }

sub _fresh {
    my ($self) = @_;
    $self->{counter}++;
    return '%tmp_' . $self->{counter};
}

# lower_value($node) -> $llvm_ref (a string like "%tmp_1" or "1" for constants)
# Recursively lowers the data sub-graph rooted at $node, accumulating
# LLVM IR instructions into $self->instructions. Returns the LLVM value
# reference (SSA name or immediate) for the node's result.
sub lower_value {
    my ($self, $node) = @_;

    # Cache: if we already lowered this node (hash-cons sharing), reuse.
    my $id = $node->id();
    return $self->{cache}{$id} if exists $self->{cache}{$id};

    my $op = $node->operation();

    if ($op eq 'Constant') {
        return $self->_lower_constant($node);
    }
    elsif ($op eq 'Add') {
        return $self->_lower_binop_int($node, 'add');
    }
    elsif ($op eq 'Subtract') {
        return $self->_lower_binop_int($node, 'sub');
    }
    elsif ($op eq 'Multiply') {
        return $self->_lower_binop_int($node, 'mul');
    }
    elsif ($op eq 'Divide') {
        # GAP — NOT lowerable runtime-free as integer division. Perl `/` is
        # ALWAYS floating-point division (3/4 == 0.75), so `sdiv i64` (which
        # truncates to 0) MISCOMPILES vs perl. A correct lowering needs a Num
        # (double) result: Coerce(Int->Num) operands + `fdiv double`. Deferred
        # to 3c (Num representation + the Int->Num coercion on `/`).
        die "GAP: op=Divide cannot lower runtime-free as i64 — Perl `/` is "
          . "float division (3/4=0.75); needs Num repr + fdiv, not sdiv. "
          . "Deferred to 3c.";
    }
    elsif ($op eq 'Modulo') {
        # GAP — NOT lowerable as bare `srem i64`. Perl `%` follows the SIGN OF
        # THE RIGHT operand (-7 % 3 == 2), whereas LLVM `srem` follows the left
        # operand (-7 srem 3 == -1) — different functions for mixed-sign
        # operands. A correct lowering needs perl-`%` sign-correction. Deferred
        # to 3c.
        die "GAP: op=Modulo cannot lower runtime-free as bare srem — Perl `%` "
          . "follows the right operand's sign (-7%3=2 not -1); needs "
          . "sign-correction. Deferred to 3c.";
    }
    elsif ($op eq 'Coerce') {
        return $self->_lower_coerce($node);
    }
    else {
        if (defined $node->representation && $node->representation eq 'Scalar') {
            die "GAP: node op=$op repr=Scalar reached LLVM backend — cannot lower runtime-free. "
              . "This value requires libperl; it is a GAP on the L corner.";
        }
        die "LLVM backend: cannot lower op=$op (not in literal-arithmetic slice)";
    }
}

sub _lower_constant {
    my ($self, $node) = @_;

    my $repr = $node->representation;
    my $val  = $node->value;

    if (!defined $repr || $repr eq 'Scalar') {
        if (!defined $repr) {
            # Unassigned representation — treat as immediate if it looks like an integer
            if (defined $val && $val =~ /\A-?\d+\z/) {
                my $ref = $self->_fresh;
                push $self->instructions->@*,
                    "  $ref = add i64 0, $val          ; Constant($val, untyped -> Int literal)";
                $self->{cache}{$node->id} = $ref;
                return $ref;
            }
        }
        die "GAP: Constant node with repr=Scalar reached LLVM backend — cannot lower runtime-free.";
    }

    if ($repr eq 'Int') {
        # Int constant -> i64 immediate via add i64 0, VALUE
        my $ref = $self->_fresh;
        push $self->instructions->@*,
            "  $ref = add i64 0, $val          ; Constant($val, repr=Int -> i64)";
        $self->{cache}{$node->id} = $ref;
        return $ref;
    }
    elsif ($repr eq 'Num') {
        # Num constant -> double immediate
        my $ref = $self->_fresh;
        push $self->instructions->@*,
            "  $ref = fadd double 0.0, $val    ; Constant($val, repr=Num -> double)";
        $self->{cache}{$node->id} = $ref;
        return $ref;
    }
    else {
        die "LLVM backend: cannot lower Constant with repr=$repr";
    }
}

sub _lower_binop_int {
    my ($self, $node, $llvm_op) = @_;

    my $repr = $node->representation;
    my $op   = $node->operation;
    die "GAP: $op with repr=Scalar reached LLVM backend" if defined $repr && $repr eq 'Scalar';

    my $inputs  = $node->inputs;
    my $lhs_ref = $self->lower_value($inputs->[0]);
    my $rhs_ref = $self->lower_value($inputs->[1]);

    my $ref = $self->_fresh;
    push $self->instructions->@*,
        "  $ref = $llvm_op i64 $lhs_ref, $rhs_ref  ; $op(repr=Int) -> i64 $llvm_op";
    $self->{cache}{$node->id} = $ref;
    return $ref;
}

sub _lower_coerce {
    my ($self, $node) = @_;

    my $from = $node->from_repr;
    my $to   = $node->to_repr;
    my $input_ref = $self->lower_value($node->inputs->[0]);
    my $ref = $self->_fresh;

    if ($from eq 'Int' && $to eq 'Num') {
        push $self->instructions->@*,
            "  $ref = sitofp i64 $input_ref to double  ; Coerce[Int->Num]";
    }
    elsif ($from eq 'Num' && $to eq 'Int') {
        push $self->instructions->@*,
            "  $ref = fptosi double $input_ref to i64  ; Coerce[Num->Int]";
    }
    elsif ($from eq 'Scalar' || $to eq 'Scalar') {
        die "GAP: Coerce involving Scalar reached LLVM backend — cannot lower runtime-free.";
    }
    else {
        die "LLVM backend: cannot lower Coerce[$from->$to]";
    }

    $self->{cache}{$node->id} = $ref;
    return $ref;
}

1;
